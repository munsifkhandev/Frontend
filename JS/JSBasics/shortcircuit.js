//Understanding Short-Circuit Evaluation in JavaScript:

//Logical AND operators:

console.log(10 && 20);
console.log(0 && 20);
console.log("" && null);
console.log(0 && null);
console.log(false && 90);

// Logical OR operators:

console.log(10 || "hehe");
console.log("" || 29);
console.log(undefined || null);
console.log(true || 21);
